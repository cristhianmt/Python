#Operadores de asignacion

a=0
a += 5 #Suma en asignacion
print(a, "Suma en asignacion")

a -= 2 #Resta en asignacion
print(a, "Resta en asignacion")

a *= 3 #Multiplicacion en asignacion
print(a, "Multiplicacion en asignacion")

a /= 3 #Division en asignacion
print(a,"Division en asignacion")

a **=2 #Poetencia en asignacion
print(a, "Poetencia en asignacion")

a %=2 # Modulo en asignacion el residuo
print(a, "Modulo en asignacion el residuo")

