#Cadenas de caracteres

cadena = "Cristhian"
print(cadena[0])#imprimir un elemento segun el indice
print(cadena[-1])#imprimir indice negativo

cadena = "Cristhian"
print(cadena[1:4]) #slicing de un numero has otro numero
print(cadena[1:])

cadena = "cristhian" #las cadenas con inmutables est quiere decir que se pueden cambiar las formas de la cadenas
print(cadena.capitalize()) #convierte el primer elemento en mayuscula
print(len(cadena))

