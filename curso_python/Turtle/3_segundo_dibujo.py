import turtle as t

t.setup(600, 600)
t.shape("turtle")
t.color('#2ECC71')


't.pendown()'    # para levanar y siguir escribiendo
't.backward()'   # metodo para ir atras
't.right()'      # rotar a la derecha

t.penup()        # levantar el lapiz y no escribir
t.forward(200)
t.pendown()
t.left(90)
t.forward(150)
t.left(90)
t.forward(400)
t.left(90)
t.forward(300)
t.left(90)
t.forward(400)
t.left(90)
t.forward(150)



t.exitonclick()
